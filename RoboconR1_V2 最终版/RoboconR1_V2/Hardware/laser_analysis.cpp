/**
 *函数返回值是无符号短整型 CRC 值 
 *待进行 CRC 校验计算的报文
 *待校验的报文长度
**/

uint16_t CRC16(uint8_t *Start_Byte, uint8_t Num_Bytes)
{
    uint8_t uchCRCHi = 0xFF; // CRC 高字节的初始化
    uint8_t uchCRCLo = 0xFF; // CRC 低字节的初始化
    uint8_t uIndex;          // CRC 查找表的指针
    while (Num_Bytes--)
    {
        uIndex = uchCRCLo ^ *Start_Byte++; // 计算 CRC
        uchCRCLo = uchCRCHi ^ auchCRCHi[uIndex];
        uchCRCHi = auchCRCLo[uIndex];
    }
    return (uchCRCHi << 8 | uchCRCLo);
}
