extern UART_HandleTypeDef huart6;
uint8_t OpenTxData[8] = {0x01,0x03,0x00,0x01,0x00,0x02,0x95,0xCB};
uint8_t CloseTxData[8] = {0x01,0x03,0x00,0x0A,0x00,0x02,0xE4,0x09};

uint32_t distance;
void Laser_Analysis(uint8_t *pData,uint8_t len) 
{
	if(pData[0]!=0x01 && pData[1]!=0x03 && pData[2]!=0x04) return;
//	if(((uint16_t)pData[7]<<8|pData[8])!= CRC16(pData,7)) return;
	distance = (uint32_t)pData[3]<<24|(uint32_t)pData[4]<<16|(uint32_t)pData[5]<<8|(uint32_t)pData[6];
}

void Laser_Open(void) 
{
	HAL_UART_Transmit_DMA(&huart6, OpenTxData, 8);
}

void Laser_Close(void) 
{
	HAL_UART_Transmit_DMA(&huart6, CloseTxData, 8);
}

uint8_t BCC(uint8_t* dat,uint16_t len)
{
 uint8_t i;
 uint8_t bcc = 0;
 for(i=0;i<len;i++)
 {
		bcc ^= dat[i];
 }
 return bcc;
}

